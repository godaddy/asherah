use blake2::{Blake2b, Digest};
use blake2::digest::consts::U32;

#[test]
fn debug_hash_raw_bytes() {
    type Blake2b256 = Blake2b<U32>;
    
    // Test "test"
    let mut hasher = Blake2b256::new();
    hasher.update(b"test");
    let result = hasher.finalize();
    
    println!("Our hash: {:?}", result.as_slice());
    println!("Expected: {:?}", &[0x92, 0x8b, 0x20, 0x36, 0x69, 0x43, 0x82, 0xaf, 0xd1, 0x1e, 0xbc, 0x0e, 0xb8, 0xba, 0x93, 0xa9, 0x3b, 0x85, 0x77, 0xa4, 0xfc, 0xf3, 0x5b, 0xbc, 0x64, 0xd5, 0x03, 0x70, 0x4e, 0x65, 0xe2, 0x02]);
}